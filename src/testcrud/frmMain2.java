    private void selectData() {
        String kolom[] = {"NoAnggota,Nama,Alamat,Jenis,JudulBuku,TanggalPinjam,TanggalKembali"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_peminjam";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try{
            while(rs.next()){
                String NoAnggota = rs.getString(1);
                String Nama = rs.getString(2);
                String Alamat = rs.getString(3);
                String Jenis = "";
                if("PT".equals(rs.getString(3))) {
                    Jenis = "Pengetahuan";
                } else{
                    Jenis = "Pemrograman";
                }
                String JudulBuku = rs.getString(4);
                String TanggalPinjam = rs.getString(5);
                String TanggalKembali = rs.getString(6);
               
             
                String data[] = {NoAnggota,Nama,Alamat,Jenis,JudulBuku,TanggalPinjam,TanggalKembali};
                dtm.addRow(data);
            }
        } catch(SQLException ex){
            Logger.getLogger(frmMain2.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblData.setModel(dtm);
    }

