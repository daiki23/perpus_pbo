    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
       if("".equals(no.getText()) || "".equals(txtNama.getText()) || "".equals(alamat_peminjam.getText()) || "".equals(status.getText()) || "".equals(tgl_pinjam) || "".equals(tgl_kembali) || "".equals(status.getText()))
        {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        } 
        else
        {   
            String jenis = "";
                if(bacaan.isSelected()){
                    jenis = "Bacaan";
                } else if(pelajaran.isSelected()){
                    jenis = "Pelajaran";
                } else if(novel.isSelected()){
                    jenis = "Novel";
                }
            String SQL = "INSERT INTO tb_pengembalian (NoAnggota,Nama,Alamat,jenis,JudulBuku,TanggalPinjam,TanggalKembali,status)" + "VALUES('" + no.getText()+"','"+ txtNama.getText()+"','"+ alamat_peminjam.getText()+"','"+ jenis +"','"+ status.getText()+"','"+ tgl_pinjam.getDate() +"','"+ tgl_kembali.getDate() +"','"+ status.getText()+"')";
            int status = KoneksiDB.execute(SQL);
            if(status==1){
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else{
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

